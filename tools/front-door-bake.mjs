// Bake the featherweight front door into a payload the Pages Function embeds, so
// every POP serves it from resident code with no origin fetch (no cold-POP MISS).
// Ships a max-quality Brotli copy for the ~all-modern-browsers br path plus the raw
// HTML fallback. Kept tiny and pure so the round-trip is unit-testable.
import { brotliCompressSync, constants } from "node:zlib";

export function bakeFrontDoor(html) {
  const br = brotliCompressSync(Buffer.from(html, "utf8"), {
    params: { [constants.BROTLI_PARAM_QUALITY]: 11 },
  });
  return { html, brB64: br.toString("base64") };
}

export function frontDoorModuleSource(html) {
  const { brB64 } = bakeFrontDoor(html);
  return (
    "// AUTO-GENERATED by tools/postbuild.mjs from renderSite(featherweight). Do not edit.\n" +
    `export const HTML = ${JSON.stringify(html)};\n` +
    `export const BR_B64 = ${JSON.stringify(brB64)};\n`
  );
}
